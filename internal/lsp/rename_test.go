func TestRename(t *testing.T) {
	t.Run("prefills and applies edit", func(t *testing.T) {
		exe, err := os.Executable()
		assert.NoError(t, err)

		dir := t.TempDir()
		path := filepath.Join(dir, "main.session")
		writeRenameLanguages(t, exe)
		assert.NoError(t, os.WriteFile(path, []byte("old\n"), 0o644))
		e := view.NewEditor(dir)
		_, err = e.OpenFile(path)
		assert.NoError(t, err)
		session := lsp.Attach(t.Context(), e)
		defer session.Close()
		doc, ok := e.FocusedDocument()
		assert.True(t, ok)
		v, ok := e.FocusedView()
		assert.True(t, ok)
		doc.SetSelectionFor(v.ID(), core.PointSelection(1))

		prefill, err := session.RenameSymbolPrefill(doc, v.ID())
		assert.NoError(t, err)
		err = session.RenameSymbol(doc, v.ID(), "new")

		assert.Equal(t, "old", prefill)
		assert.NoError(t, err)
		assert.Equal(t, "new\n", doc.Text().String())
	})
}

